import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const bgmSrc = path.resolve(root, 'BGM')
const bgmPublic = path.resolve(root, 'public/BGM')
const manifestOut = path.resolve(root, 'src/phone/apps/wechat/dating/vnBgmManifest.generated.ts')

const AUDIO_EXT = /\.(mp3|wav|ogg|m4a|flac|aac)$/i

function cleanBgmName(raw) {
  return String(raw || '')
    .trim()
    .replace(/\.[A-Za-z0-9]+$/u, '')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function collectAudioFiles(dir) {
  const files = []
  if (!fs.existsSync(dir)) return files
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, ent.name)
    if (ent.isDirectory()) files.push(...collectAudioFiles(full))
    else if (AUDIO_EXT.test(ent.name)) files.push(ent.name)
  }
  return files
}

/** Windows 上 fs.cpSync 对中文路径可能触发 native crash，逐文件复制更稳 */
function copyBgmToPublic() {
  if (!fs.existsSync(bgmSrc)) return
  fs.mkdirSync(bgmPublic, { recursive: true })
  for (const fileName of collectAudioFiles(bgmSrc)) {
    fs.writeFileSync(path.join(bgmPublic, fileName), fs.readFileSync(path.join(bgmSrc, fileName)))
  }
}

function writeManifest(fileNames) {
  const sorted = [...fileNames].sort((a, b) => a.localeCompare(b, 'zh-CN'))
  const entries = sorted
    .map((fileName) => {
      const name = cleanBgmName(fileName)
      const encoded = encodeURIComponent(fileName)
      return `  { name: ${JSON.stringify(name)}, fileName: ${JSON.stringify(fileName)}, url: \`\${import.meta.env.BASE_URL}BGM/${encoded}\` },`
    })
    .join('\n')

  const content =
    `// Generated by scripts/sync-vn-bgm-manifest.mjs — do not edit\n` +
    `export const VN_BGM_MANIFEST: { name: string; fileName: string; url: string }[] = [\n` +
    `${entries}\n` +
    `]\n`

  fs.mkdirSync(path.dirname(manifestOut), { recursive: true })
  fs.writeFileSync(manifestOut, content, 'utf8')
}

function main() {
  if (!fs.existsSync(bgmSrc)) {
    writeManifest([])
    return
  }

  copyBgmToPublic()
  writeManifest(collectAudioFiles(bgmSrc))
}

main()
