/** 具体的更新细节 */
export type UpdateDetail = {
  /** 更新内容（highlight 之后的正文；无 highlight 时为整段） */
  text: string
  /** 句首高亮词（如「手机里多了微博广场：」） */
  highlight?: string
}

/** 功能模块（如「微博广场」「系统演进录」） */
export type UpdateModule = {
  moduleName: string
  /** 仅「新增」模块使用：玩家怎么找到这个功能，如「微信 › 发现 › 微博广场」 */
  entryPath?: string
  items: UpdateDetail[]
}

/** 顶层更新类型（新增 / 优化 / 修复） */
export type UpdateCategory = {
  type: 'feature' | 'optimization' | 'fix'
  modules: UpdateModule[]
}

export type VersionRecord = {
  version: string
  date: string
  title: string
  categories: UpdateCategory[]
}

/** 便捷：句首「…：」拆成 highlight + text */
function d(full: string): UpdateDetail {
  const idx = full.indexOf('：')
  if (idx > 0 && idx <= 24) {
    return {
      highlight: full.slice(0, idx + 1),
      text: full.slice(idx + 1).trimStart(),
    }
  }
  return { text: full }
}

